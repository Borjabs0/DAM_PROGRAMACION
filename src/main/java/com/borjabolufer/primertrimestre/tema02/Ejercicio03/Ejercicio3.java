package com.borjabolufer.primertrimestre.tema02.Ejercicio03;

public class Ejercicio3 {
    public int calcularAreaRectangulo(int altura, int anchura){
        return  altura * anchura;
    }
    public int calcularPerimetroRectangulo(int altura, int anchura){
        return  (altura * 2) + (anchura * 2);
    }
}
